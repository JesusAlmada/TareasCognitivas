       public void cuadrantes() {
        String c = e.Cadena();
        if (EsPar(c)) {
            if(arriba){
                Cuadrante1tf.setText(c);
            }
            
        } else if(!EsPar(c)){
            if(arriba){
              Cuadrante2tf.setText(c);
              
            }
            arriba=false;
        }else if(EsVocal(c)){
            if(!arriba){
                Cuadrante3tf.setText(c);
            }
        } else if(!EsVocal(c)){
            if(!arriba){
                Cuadrante4tf.setText(c);
            }
            arriba=true;
        }

    }
    

    public boolean EsPar(String cadena) {
        int num = Integer.parseInt(cadena.substring(1));
        if (num % 2 == 0) {
            return true;
        }
        return false;
    }

    public boolean EsVocal(String cadena) {
        String s = cadena.substring(0);
        String v[] = {"A", "E", "I", "O", "U"};
        
        for (int i = 0; i < v.length; i++) {
            if (v[i].equals(s)) {
                return true;
            } else {
                return false;
            }
        }
        
        return false;
    }
