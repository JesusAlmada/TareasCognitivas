    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed

        btnIniciar.setEnabled(false);
        estimuloNuevo = au.nuevoEstimulo();
        if (isInicial) {
            estimuloViejo = estimuloNuevo; //Estimulo base
            isInicial = false;
            timer.start();
            iluminarCualFue(estimuloNuevo, Color.YELLOW);
            diff++;
        } else {
            timer.start();
            iluminarCualFue(estimuloNuevo, Color.YELLOW);
            diff++;
        }


    }//GEN-LAST:event_btnIniciarActionPerformed

    private void btnPresionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPresionarActionPerformed
        btnPresionar.setEnabled(false);
        if (timer.isRunning()) {

            if (n == diff) {
                if (estimuloNuevo.equals(estimuloViejo)) {
                    iluminarCualFue(estimuloNuevo, Color.GREEN);
                } else {
                    iluminarCualFue(estimuloNuevo, Color.RED);
                }
            } else {
                iluminarCualFue(estimuloNuevo, Color.RED);
            }

            diff = 0;
            isInicial = true;

            estimuloViejo = null;
        }

    }//GEN-LAST:event_btnPresionarActionPerformed

