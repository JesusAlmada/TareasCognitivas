    private int iluminarCualFue(boolean estimulo[], Color color) {
        int cual = -1;
        for (int i = 0; i < estimulo.length; i++) {
            if (estimulo[i]) {
                cual = i + 1;
                break;
            }
        }
        switch (cual) {
            case 1:
                cuadro1.setBackground(color);
                break;
            case 2:
                cuadro2.setBackground(color);
                break;
            case 3:
                cuadro3.setBackground(color);
                break;
            case 4:
                cuadro4.setBackground(color);
                break;
            case 5:
                cuadro5.setBackground(color);
                break;
            case 6:
                cuadro6.setBackground(color);
                break;
            case 7:
                cuadro7.setBackground(color);
                break;
            case 8:
                cuadro8.setBackground(color);
                break;
            default:
                btnIniciar.setText("algo paso lol");
                break;
        }

        return cual;
    }

